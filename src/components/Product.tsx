    const updateProduct = useCart((state) => state.updateProduct)
    const products = useCart((state) => state.products)
    const addNewProduct = (id: string) => {
        const product = products.find(p => p.productId === id);
        if (product) updateProduct(id)
        else addProduct({
            id: uuidv4(),
            productId: id,
            quantity: 1
        })
    }
